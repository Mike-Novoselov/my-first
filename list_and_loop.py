# цикл for
x=[5,20,67,90] # создаем список
for y in x: # команда для цикла что у применяется к списку х
    y*=2 # выполняем цикл у * 2 к каждому числу из списка х
    print(y) # выводим ответ

# цикл while
q=1
while(q<10): # цикл while будет выполняться пока q меньше 10
    print(q*q) # при этом ответ будет выводится q*q (квадрат числа)
    q=q+1 # после выполнения цикла q будет увеличиватся на единицу, что бы добратся до 10 и остановится

