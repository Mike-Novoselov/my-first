a=[67,5,90,20,30] # создал численный список
b=['Миша', 'Ваня', 'Лена', 'Марина', 'Арнольд',] # создал текстовый список
# выводим отдельные данные из спика, нумирация начинается с 0 (0== первому значению из списка)
print(a[2]) 
